        public ActionResult ChangeStatus(int? id)
        {
            //Kiểm tra login
            var checkLogin = CheckLogin();
            if (checkLogin != null)
            {
                return checkLogin;
            }

            //Kiểm tra tồn tại của listing
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Listing listing = db.Listings.Find(id);
            if (listing == null)
            {
                return HttpNotFound();
            }

            return View(listing);
        }
        [HttpPost]
        public ActionResult ChangeStatus(int? id, string Status, string ApprovalNote)
        {
            try
            {
                //Lấy UserID từ session
                int UserID = Convert.ToInt32(Session["UserID"]);

                //Kiểm tra tồn tại của Listing
                if (id == null)
                {
                    return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
                }
                Listing listing = db.Listings.Find(id);
                if (listing == null)
                {
                    return HttpNotFound();
                }

                //Tìm EmployeeID theo UserID
                var employee = db.Employees.FirstOrDefault(e => e.UserID == UserID);
                if (employee == null)
                {
                    ViewBag.Error = "Không có nhân viên này trong Employee";
                    return View();
                }

                //Thay đổi dữ liệu của listing
                listing.Status = Status;
                listing.ApprovedAt = DateTime.Now;
                listing.EmployeeID = employee.EmployeeID;
                listing.ApprovalNote = ApprovalNote;
                db.Entry(listing).State = EntityState.Modified;
                db.SaveChanges();

                return RedirectToAction("PendingListing");
            }
            catch (Exception ex)
            {
                ViewBag.Error = ex.InnerException.InnerException.Message;
                return View();
            }
        }

        //Kiểm tra login
        //Author: le Quang Dung
        private ActionResult CheckLogin()
        {
            //Kiểm tra đăng nhập hay chưa
            if (Session["UserID"] != null)
            {
                //Chuyển UserID trong session sang int
                bool parse = int.TryParse(Session["UserID"].ToString(), out int UserID);
                if (!parse)
                {
                    return RedirectToAction("Login", "UserAccounts", new { area = "" });
                }
                //Tìm kiếm user trong UserAccounts
                UserAccount user = db.UserAccounts.Where(u => u.UserID == UserID).FirstOrDefault();
                if (user == null)
                {
                    return RedirectToAction("Login", "UserAccounts", new { area = "" });
                }
                //Chuyển hướng
                if (user.Role == "Admin" || user.Role == "Employee")
                {
                    //Admin và Employee thì tiếp tục
                    return null;
                }
                else if (user.Role == "Agent" || user.Role == "Customer")
                {
                    //Agent và Customer vào giao diện Customer
                    return RedirectToAction("Index", "Home", new { area = "" });
                }
            }
            return RedirectToAction("Login", "UserAccounts", new { area = "" });
        }
