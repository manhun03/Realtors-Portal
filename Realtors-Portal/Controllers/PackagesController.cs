        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "PackageID,PackageName,Description,PackageType,DurationDays,Price,DiscountPercent,Features,Multiplier,IsActive,CreatedAt,UpdatedAt")] Package package)
