using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web.Mvc;
using Realtors_Portal.Models;

namespace Realtors_Portal.Controllers
{
    public class PackagesController : Controller
    {
        private RealtorsPortalEntities db = new RealtorsPortalEntities();

        //Buy package
        //Author: Le Quang Dung
        public ActionResult Buy(int id)
        {
            //Check for package's existence in db
            var package = db.Packages.Find(id);
            if (package == null) 
            { 
                return HttpNotFound(); 
            }

            //Redirect to payment handling
            return RedirectToAction("Checkout", "Payments", new { PackageID = id });
        }
        //=============================================CRUD=====================================================
        //This section was auto-generated by Entity Framework
        // GET: Packages
        public ActionResult Index()
        {
            return View(db.Packages.ToList());
        }

        // GET: Packages/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Package package = db.Packages.Find(id);
            if (package == null)
            {
                return HttpNotFound();
            }
            return View(package);
        }

        // GET: Packages/Create
        public ActionResult Create()
